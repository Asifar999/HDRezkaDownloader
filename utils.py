import time, base64, os

def getTime():
	return round(time.time() * 1000)

def clear():
	os.system('cls' if os.name == 'nt' else 'clear')

def clearTrash(data):
	trashCodesSet = [
		'@@',  '@#',  '@!',  '@^',  '@$',  '#@',  '##',  '#!',  '#^',  '#$',
		'!@',  '!#',  '!!',  '!^',  '!$',  '^@',  '^#',  '^!',  '^^',  '^$',
		'$@',  '$#',  '$!',  '$^',  '$$',  '@@@', '@@#', '@@!', '@@^', '@@$',
		'@#@', '@##', '@#!', '@#^', '@#$', '@!@', '@!#', '@!!', '@!^', '@!$',
		'@^@', '@^#', '@^!', '@^^', '@^$', '@$@', '@$#', '@$!', '@$^', '@$$',
		'#@@', '#@#', '#@!', '#@^', '#@$', '##@', '###', '##!', '##^', '##$',
		'#!@', '#!#', '#!!', '#!^', '#!$', '#^@', '#^#', '#^!', '#^^', '#^$',
		'#$@', '#$#', '#$!', '#$^', '#$$', '!@@', '!@#', '!@!', '!@^', '!@$',
		'!#@', '!##', '!#!', '!#^', '!#$', '!!@', '!!#', '!!!', '!!^', '!!$',
		'!^@', '!^#', '!^!', '!^^', '!^$', '!$@', '!$#', '!$!', '!$^', '!$$',
		'^@@', '^@#', '^@!', '^@^', '^@$', '^#@', '^##', '^#!', '^#^', '^#$',
		'^!@', '^!#', '^!!', '^!^', '^!$', '^^@', '^^#', '^^!', '^^^', '^^$',
		'^$@', '^$#', '^$!', '^$^', '^$$', '$@@', '$@#', '$@!', '$@^', '$@$',
		'$#@', '$##', '$#!', '$#^', '$#$', '$!@', '$!#', '$!!', '$!^', '$!$',
		'$^@', '$^#', '$^!', '$^^', '$^$', '$$@', '$$#', '$$!', '$$^', '$$$'
	]

	arr = data.replace('#h', '')
	arr = arr.split('//_//')
	trashString = ''.join(arr)

	for i in trashCodesSet:
		temp = base64.b64encode(i.encode())
		trashString = trashString.replace(temp.decode(), '')

	return base64.b64decode(trashString).decode()